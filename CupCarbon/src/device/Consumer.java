package device;

import map.Layer;

public class Consumer extends Thread {

	private DeviceWithWithoutRadio s = null;
	private int n ;
	
	public Consumer(DeviceWithWithoutRadio s, int n) {
		this.s = s ;
		this.n = n ;
	}
	
	@Override
	public void run() {
		for (int i = 0; i < n; i++) {
			s.consume(100000);
			Layer.getMapViewer().repaint();
			try {
				sleep(Device.moveSpeed);
			} catch (InterruptedException e) {
				e.printStackTrace();
			}
		}
	}
	
}
